function less_than ( val1, val2 ) {
   if ( val1 < val2 ) {
      return 1;
   } else {
      return 0;
   }
}

function greater_than ( val1, val2) {
   if ( val1 > val2 ) {
      return 1;
   } else {
      return 0;
   }
}

function less_or_equal ( val1, val2 ) {
   if ( val1 <= val2 ) {
      return 1;
   } else {
      return 0;
   }
}

function greater_or_equal ( val1, val2) {
   if ( val1 >= val2 ) {
      return 1;
   } else {
      return 0;
   }
}
